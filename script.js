function applyTranslations(lang) {
  const dict = translations[lang] || translations.en;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    const key = el.getAttribute("data-i18n-placeholder");
    if (dict[key]) {
      el.setAttribute("placeholder", dict[key]);
    }
  });

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.body.classList.toggle("rtl", lang === "ar");

  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("is-active", btn.dataset.lang === lang);
  });

  localStorage.setItem("flowLang", lang);
}

function initNav() {
  const toggle = document.querySelector("[data-mobile-toggle]");
  const nav = document.querySelector(".nav-links");
  if (!toggle || !nav) return;

  toggle.addEventListener("click", () => nav.classList.toggle("is-open"));
  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => nav.classList.remove("is-open"));
  });

  const page = document.body.dataset.page;
  const currentHref = page === "home" ? "index.html" : `${page}.html`;
  nav.querySelectorAll("a").forEach((link) => {
    if (link.getAttribute("href") === currentHref) {
      link.classList.add("is-current");
    }
  });
}

function initReveal() {
  const revealEls = document.querySelectorAll(".reveal");
  const obs = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("in-view");
          obs.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.15 }
  );
  revealEls.forEach((el) => obs.observe(el));
}

function initTestimonials() {
  const slider = document.querySelector("[data-testimonials]");
  if (!slider) return;

  const cards = [...slider.querySelectorAll(".testimonial-card")];
  const dots = [...slider.querySelectorAll(".dot")];
  let current = 0;

  const show = (index) => {
    current = index;
    cards.forEach((c, i) => c.classList.toggle("is-active", i === index));
    dots.forEach((d, i) => d.classList.toggle("is-active", i === index));
  };

  dots.forEach((dot) => {
    dot.addEventListener("click", () => show(Number(dot.dataset.slide)));
  });

  setInterval(() => {
    show((current + 1) % cards.length);
  }, 4500);
}

function initContactForm() {
  const form = document.querySelector("[data-contact-form]");
  if (!form) return;

  form.addEventListener("submit", (e) => {
    e.preventDefault();
    const lang = localStorage.getItem("flowLang") || "en";
    alert(translations[lang].form_success);
    form.reset();
  });
}

function initLanguageSwitcher() {
  const buttons = document.querySelectorAll(".lang-btn");
  buttons.forEach((btn) => {
    btn.addEventListener("click", () => applyTranslations(btn.dataset.lang));
  });
}

function initHeaderScroll() {
  const header = document.querySelector(".site-header");
  if (!header) return;

  const toggleHeaderState = () => {
    header.classList.toggle("is-scrolled", window.scrollY > 20);
  };

  toggleHeaderState();
  window.addEventListener("scroll", toggleHeaderState, { passive: true });
}

document.addEventListener("DOMContentLoaded", () => {
  const lang = localStorage.getItem("flowLang") || "en";
  applyTranslations(lang);
  initLanguageSwitcher();
  initHeaderScroll();
  initNav();
  initReveal();
  initTestimonials();
  initContactForm();

  const year = document.getElementById("year");
  if (year) year.textContent = new Date().getFullYear();
});
